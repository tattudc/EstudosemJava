public class Program
{
	public static void main(String[] args) {
	    //Esse é um comentário de uma linha
		//Variaveis inteira
		int numero;
		numero = 10;
		int numero2 = 12;
		//Strings são nomes e caracteres
		String minhaVariavelString = "Tarcisio Câmara";
		//byte são variaveis que vai de -127 a 128
		byte minhaVariavelByte = 20;
		//short vai de -32768 a 32768
		short minhaVariavelShort = 3200;
		//int vai de -2147483648 a 2147483648
		int minhaVariavelInt = 33000;
		//long vai de numeros bem grandes
		long minhaVariavelLong = 10000000000000L;
		//float usa decimais
		float preco = 2.50f;
		//double da precisao acima das 7 casas decimais
		double preco2 = 2.1231231231;
		//boolean é estados de true e false
		boolean minhaVariavelBoolean = true;
		
		//Comando system.out.println é para printar algo na tela
	    System.out.println(numero);
		System.out.println(numero2);
		System.out.println(minhaVariavelByte);
		System.out.println(minhaVariavelShort);
		System.out.println(minhaVariavelInt);
		System.out.println(minhaVariavelLong);
		System.out.println(preco);
		System.out.println(preco2);
		System.out.println(minhaVariavelBoolean);
		System.out.println(minhaVariavelString);
		
	}
}
public class HelloWorld
{
    public static void main(String[] args) {
		int num1 = 10;
		int num2 = 5;
		int resultado;
		//Somar (+)
		//Subtração (-)
		//Multiplicação (*)
		//Divisão (/)
		//Resto da divisão ou modulo(%)
		resultado = num1 + num2;
		System.out.println(resultado);
		resultado = num1 - num2;
		System.out.println(resultado);
		resultado = num1 * num2;
		System.out.println(resultado);
		resultado = num1 / num2;
		System.out.println(resultado);
		resultado = num1 % num2;
		System.out.println(resultado);
    }
}